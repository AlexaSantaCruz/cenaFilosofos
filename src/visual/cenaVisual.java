    /**
     * @param args the command line arguments
     */
    
private void updateUI(Filosofo[] filosofos) {
    for (int i = 0; i < filosofos.length; i++) {
        Filosofo filosofo = filosofos[i];
        JLabel accionLabel = getAccionLabel(i + 1); // Obtener el JLabel correspondiente al fil√≥sofo

        if (filosofo.isEstaComiendo()) {
            // Cambiar la imagen en el JLabel para el fil√≥sofo que est√° comiendo
            accionLabel.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("comiendo.png")).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH)));
        } else {
            // Cambiar la imagen en el JLabel para el fil√≥sofo que est√° pensando
            accionLabel.setIcon(new ImageIcon(new ImageIcon(getClass().getResource("pensando.png")).getImage().getScaledInstance(50, 50, Image.SCALE_SMOOTH)));
        }
    }
}

private JLabel getAccionLabel(int filosofoId) {
    switch (filosofoId) {
        case 1:
            return accion1;
        case 2:
            return accion2;
        case 3:
            return accion3;
        case 4:
            return accion4;
        case 5:
            return accion5;
        default:
            return null;
    }
}

    
